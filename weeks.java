class Weeks
{
	public static void main(String[]args)
	{
	String day="tuesday";
	String activity="";
	if(day=="monday")
	{
	activity="work";
	}
	else if(day=="tuesday")
	{
	activity="spending time with family";
	}
	else if(day=="wednesday")
	{
	activity="games";
	}
	else if(day=="thursday")
	{
	activity="meeting friends";
	}
	else if(day=="friday")
	{
	activity="shopping";
	}
	else if(day=="saturday")
	{
	activity="praying";
	}
	else if(day=="sunday")
	{
	activity="taking rest";
	}
	System.out.println(activity);
	}
}
	